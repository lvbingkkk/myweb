<script>
    import Favorite from "./Favorite";
    export default {
        props: ['attributes'],

        components:{Favorite},


        data() {
            return{
                editing:false,
                body:this.attributes.body
            };
        },

        methods:{
            update() {
                axios.patch('/replies/' + this.attributes.id, {
                    body: this.body
                });

                this.editing = false;

                flash('已更新 ！！！');
            },

            destroy() {
                axios.delete('/replies/' + this.attributes.id)

                $(this.$el).fadeOut(300, () => {
                    flash('你的回复已删除！！！');

                });
            }
        }

    }
</script>
